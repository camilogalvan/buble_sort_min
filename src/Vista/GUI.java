    private void generarAleatorio() {
        int length=this.so.getSecuencia().getCapacidad();
        Random x = new Random();
        for (int i = 0; i < length; i++) {
            int numRandom = x.nextInt(length);
            so.getSecuencia().insertar(numRandom);
            so.getSecuencia1().insertar(numRandom);
        }
    }

